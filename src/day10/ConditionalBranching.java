package day10;

public class ConditionalBranching {
	public static void main(String[] args) {
		int number = 5;
		int numberTwo = 6;
		//       5 > 6 -> false
		if (number > numberTwo) {
			System.out.println("In line 9");
		} else {
			System.out.println("In line 11");
		}
		
		System.out.println("The end");
	}
}
