package day06;

public class Subtraction {
	public static void main(String[] args) {
		int numOne = 55;
		int numTwo = 29;
		//    26   =     55 - 29
		int result = numOne - numTwo;
		
		//                 "Result: 26"
		System.out.println("Result: " + result);

		// Formatting spaces
		// MAC: command + shift + F
		// Windows: Ctrl + shift + F
	}
}
